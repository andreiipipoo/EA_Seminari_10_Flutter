# Seminari 10: Flutter

## Tasques

- **Crear servei i controlador per a la funció Modificar**
- **Crear la funció Modificar usuari amb un botó en els mateixos**

## Descripció

En aquest seminari, hem aprés:

1. Crear un servei per a la funció de modificació.
2. Desenvolupar un controlador per gestionar la modificació.
3. Implementar la funció de modificació d'usuari amb un botó.

## Objectius

- Millorar les habilitats en Flutter.
- Aprendre a gestionar serveis i controladors.
- Implementar funcionalitats interactives en una aplicació Flutter.

